# function: 用欧式距离计算相似度
# input: 两个列向量
# return: 相似度 0 到 1	   
def ecludSim(inA,inB):
    return 1.0/(1.0 + la.norm(inA - inB))

# function: 用皮尔逊相关系数计算相似度
# input: 两个列向量
# return: 相似度 0 到 1		
def pearsSim(inA,inB):
    if len(inA) < 3 : return 1.0
    return 0.5+0.5*corrcoef(inA, inB, rowvar = 0)[0][1]

# function: 用余弦相似度计算相似度
# input: 两个列向量
# return: 相似度 0 到 1		
def cosSim(inA,inB):
    num = float(inA.T*inB)
    denom = la.norm(inA)*la.norm(inB)
    return 0.5+0.5*(num/denom)

# function: 预测 user 对 item 的评分
# input: dataMat(数据矩阵), user(用户编号), simMeas(相似度计算方法), item(物品编号)
# return: user 对 item 的评分	
def standEst(dataMat, user, simMeas, item):
    n = shape(dataMat)[1]
    simTotal = 0.0; ratSimTotal = 0.0
    for j in range(n):
        userRating = dataMat[user,j]
        if userRating == 0: continue
        overLap = nonzero(logical_and(dataMat[:,item].A>0, \
                                      dataMat[:,j].A>0))[0]
        #print("overLap: ", overLap)									  
        if len(overLap) == 0: similarity = 0
        else: similarity = simMeas(dataMat[overLap,item], \
                                   dataMat[overLap,j])
        #print("dataMat[overLap,item] :", dataMat[overLap,item])
        #print("dataMat[overLap,j] :", dataMat[overLap,j])
        print('the %d and %d similarity is: %f' % (item, j, similarity))
        simTotal += similarity
        ratSimTotal += similarity * userRating
    if simTotal == 0: return 0
    else: return ratSimTotal/simTotal
 
# function: 预测 user 对 item 的评分(和上面的standEst函数不同的地方在于这里使用了 SVD)
# input: dataMat(数据矩阵), user(用户编号), simMeas(相似度计算方法), item(物品编号)
# return: user 对 item 的评分 
def svdEst(dataMat, user, simMeas, item):
    n = shape(dataMat)[1]
    simTotal = 0.0; ratSimTotal = 0.0
    U,Sigma,VT = la.svd(dataMat)
    Sig4 = mat(eye(4)*Sigma[:4]) #arrange Sig4 into a diagonal matrix
    xformedItems = dataMat.T * U[:,:4] * Sig4.I  #create transformed items
    for j in range(n):
        userRating = dataMat[user,j]
        if userRating == 0 or j==item: continue
        similarity = simMeas(xformedItems[item,:].T,\
                             xformedItems[j,:].T)
        print('the %d and %d similarity is: %f' % (item, j, similarity))
        simTotal += similarity
        ratSimTotal += similarity * userRating
    if simTotal == 0: return 0
    else: return ratSimTotal/simTotal

# function: 推荐 user 可能评分最高的前 N 个物品
# input: dataMat(数据矩阵), user(用户编号), N, simMeas(相似度计算方法), estMethod(预测函数)
# return: user 对 item 的评分	
def recommend(dataMat, user, N=3, simMeas=cosSim, estMethod=standEst):
    unratedItems = nonzero(dataMat[user,:].A==0)[1]#find unrated items 
    if len(unratedItems) == 0: return 'you rated everything'
    itemScores = []
    for item in unratedItems:
        estimatedScore = estMethod(dataMat, user, simMeas, item)
        itemScores.append((item, estimatedScore))
    return sorted(itemScores, key=lambda jj: jj[1], reverse=True)[:N]

# function: 打印矩阵
# input: inMat(数据矩阵), thresh(threshold)
# return: None
def printMat(inMat, thresh=0.8):
    for i in range(32):
        for k in range(32):
            if float(inMat[i,k]) > thresh:
                print(1, end="")
            else: print(0, end="")
        print('')

# function: 对原始图像进行SVD分解并重构图像
# input: numSV(奇异值个数), thresh(threshold)
# return: None		
def imgCompress(numSV=3, thresh=0.8):
    myl = []
    for line in open('0_5.txt').readlines():
        newRow = []
        for i in range(32):
            newRow.append(int(line[i]))
        myl.append(newRow)
    myMat = mat(myl)
    print("****original matrix******")
    printMat(myMat, thresh)
    U,Sigma,VT = la.svd(myMat)
    SigRecon = mat(zeros((numSV, numSV)))
    for k in range(numSV):#construct diagonal matrix from vector
        SigRecon[k,k] = Sigma[k]
    reconMat = U[:,:numSV]*SigRecon*VT[:numSV,:]
    print("****reconstructed matrix using %d singular values******" % numSV)
    printMat(reconMat, thresh)